drop table IF EXISTS clinvar_entry;
CREATE TABLE `clinvar_entry` (
  `ID` int NOT NULL AUTO_INCREMENT,
  `CHR_POS` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL, -- CHR_POSSTART_POSEND
  `MUTATION_NO` VARCHAR(50) NOT NULL,
  `CLINVAR_ID` int, -- key in clinvar
  `REF` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin,
  `ALT` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin,
  `AF_ESP` float DEFAULT -1,
  `AF_EXAC` float DEFAULT -1,
  `AF_TGP` float DEFAULT -1,
  `ALLELEID` int DEFAULT -1,
  `CLNDN` TEXT,   
  `CLNDNINCL` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNDISDB` TEXT,
  `CLNDISDBINCL` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNHGVS` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNREVSTAT` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNSIG` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNSIGCONF` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNSIGINCL` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNVC` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNVCSO` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `CLNVI` TEXT,
  `DBVARID` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `GENEINFO` TEXT,
  `MC` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `ORIGIN` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `RS` VARCHAR(255) CHARACTER SET utf8 COLLATE utf8_bin DEFAULT '',
  `SSR` int DEFAULT -1,
  `UPDATE_DATE` DATE,
  PRIMARY KEY(`ID`),
  KEY(`CHR_POS`,`MUTATION_NO`,`CLINVAR_ID`)
);

